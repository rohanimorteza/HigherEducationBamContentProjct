package com.example.myhighereducationofcomplexbam002.Model;

public class Professor {

    public String getIdProf() {
        return idProf;
    }

    public void setIdProf(String idProf) {
        this.idProf = idProf;
    }

    public String getNameProf() {
        return NameProf;
    }

    public void setNameProf(String nameProf) {
        NameProf = nameProf;
    }

    public String getFacultyProf() {
        return FacultyProf;
    }

    public void setFacultyProf(String facultyProf) {
        FacultyProf = facultyProf;
    }

    public byte[] getAxProf() {
        return axProf;
    }

    public void setAxProf(byte[] axProf) {
        this.axProf = axProf;
    }

    private String idProf;
    private String NameProf;
    private String FacultyProf;
    private byte[] axProf;


}
